export async function setAddresses(currentState: unknown, formData: FormData) {
  try {
    if (!formData) {
      throw new Error("No form data found when setting addresses")
    }
    const cartId = getCartId()
    if (!cartId) {
      throw new Error("No existing cart found when setting addresses")
    }

    const data = {
      shipping_address: {
        first_name: formData.get("shipping_address.first_name"),
        last_name: formData.get("shipping_address.last_name"),
        address_1: formData.get("shipping_address.address_1"),
        address_2: formData.get("shipping_address.address_2") || "",
        company: formData.get("shipping_address.company"),
        postal_code: formData.get("shipping_address.postal_code"),
        city: formData.get("shipping_address.city"),
        country_code: formData.get("shipping_address.country_code"),
        province: formData.get("shipping_address.province"),
        phone: formData.get("shipping_address.phone"),
      },
      email: formData.get("email"),
    } as any

    const sameAsBilling = formData.get("same_as_billing")
    if (sameAsBilling === "on") data.billing_address = data.shipping_address

    if (sameAsBilling !== "on")
      data.billing_address = {
        first_name: formData.get("billing_address.first_name"),
        last_name: formData.get("billing_address.last_name"),
        address_1: formData.get("billing_address.address_1"),
        address_2: formData.get("billing_address.address_2") || "",
        company: formData.get("billing_address.company"),
        postal_code: formData.get("billing_address.postal_code"),
        city: formData.get("billing_address.city"),
        country_code: formData.get("billing_address.country_code"),
        province: formData.get("billing_address.province"),
        phone: formData.get("billing_address.phone"),
      }
    await updateCart(data)
  } catch (e: any) {
    return e.message
  }

  redirect(
    `/${formData.get("shipping_address.country_code")}/checkout?step=delivery`
  )
}

type PlaceOrderSuccess = {
  success: true
  cart?: HttpTypes.StoreCart
}

type PlaceOrderFailure = {
  success: false
  error: string
}

export type PlaceOrderResult = PlaceOrderSuccess | PlaceOrderFailure

export async function placeOrder(): Promise<PlaceOrderResult> {
  const cartId = getCartId()
  if (!cartId) {
    return {
      success: false,
      error: "No existing cart found when placing an order.",
    }
  }

  try {
    const completion = await sdk.store.cart.complete(cartId, {}, getAuthHeaders())

    revalidateTag("cart")

    const completionType = completion?.type
    const orderData =
      completionType === "order"
        ? ((completion as any).order ?? (completion as any).data ?? null)
        : null
    const cartData =
      completionType === "cart"
        ? ((completion as any).cart ?? (completion as any).data ?? null)
        : null

    if (orderData) {
      if (!orderData.id) {
        return {
          success: false,
          error:
            "Your order was created, but we couldn't determine the confirmation page. Please check your email for the receipt.",
        }
      }

      const shippingCountry =
        orderData.shipping_address?.country_code?.toLowerCase()
      const billingCountry =
        orderData.billing_address?.country_code?.toLowerCase()
      const regionCountry =
        orderData.region?.countries?.[0]?.iso_2?.toLowerCase()
      const defaultCountry =
        process.env.NEXT_PUBLIC_DEFAULT_REGION?.toLowerCase() ?? "us"

      const redirectCountryCode =
        shippingCountry ?? billingCountry ?? regionCountry ?? defaultCountry

      const searchParams = new URLSearchParams()
      if (orderData.email) {
        searchParams.set("email", orderData.email)
      }
      if (orderData.display_id) {
        searchParams.set("orderNo", `${orderData.display_id}`)
      }
      if (orderData.id) {
        searchParams.set("orderId", orderData.id)
      }

      const queryString = searchParams.toString()
      const confirmationPath = `/${redirectCountryCode}/order/confirmed/${orderData.id}${
        queryString ? `?${queryString}` : ""
      }`

      removeCartId()
      redirect(confirmationPath)
    }

    if (completionType === "cart") {
      const errorMessage =
        (completion as any).error?.message ||
        "We couldn't complete your order. Please double-check your payment details and try again."

      return {
        success: false,
        error: errorMessage,
      }
    }

    return {
      success: true,
      cart: cartData,
    }
  } catch (error: any) {
    if (error?.digest === "NEXT_REDIRECT") {
      throw error
    }

    const message = extractMedusaErrorMessage(error)

    return {
      success: false,
      error: message,
    }
  }
}

/**
 * Updates the countrycode param and revalidates the regions cache
 * @param regionId
 * @param countryCode
 */
export async function updateRegion(countryCode: string, currentPath: string) {
  const cartId = getCartId()
  const region = await getRegion(countryCode)

  if (!region) {
    throw new Error(`Region not found for country code: ${countryCode}`)
  }

  if (cartId) {
    await updateCart({ region_id: region.id })
    revalidateTag("cart")
  }

  revalidateTag("regions")
  revalidateTag("products")

  redirect(`/${countryCode}${currentPath}`)
}
